/**
 * Enum định nghĩa các loại trận đấu PVP trong game.
 * @author Lucifer
 */
public enum TYPE_PVP {

    /**
     * Loại trận đấu thách đấu giữa hai người chơi.
     */
    THACH_DAU,

    /**
     * Loại trận đấu trả thù để đối đầu với kẻ thù.
     */
    TRA_THU,

    /**
     * Loại trận đấu trong sự kiện Đại Hội Võ Thuật.
     */
    DAI_HOI_VO_THUAT
}